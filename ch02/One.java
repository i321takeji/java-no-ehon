package ch02;

/**
 * 計算の演算子 (2), p.30
 */

class One {
    public static void main(String[] args) {
        int a = 1;
        System.out.println("はじめは " + a + " でした．");

        a++;
        System.out.println("1 増えて " + a + " になりました．");

        a--;
        System.out.println("1 減って " + a + " に戻りました．");
    }
}
