package ch02;

/**
 * 計算の演算子 (1), p.28
 */

public class Calculation {
    public static void main(String[] args) {
        System.out.println("5+5 は " + (5 + 5) + " です．");
        System.out.println("5-5 は " + (5 - 5) + " です．");
        System.out.println("5×5 は " + (5 * 5) + " です．");
        System.out.println("5÷5 は " + (5 / 5) + " です．");
        System.out.println("5÷3 の余りは " + (5 % 3) + " です．");
    }
}
